    private void tblSHSMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSHSMouseClicked
        // TODO add your handling code here:
        point = tblSHS.rowAtPoint(evt.getPoint());
        DefaultTableModel shsModel = (DefaultTableModel) tblSHS.getModel();
        
        if(didClick == true && !(point == prevPoint)){
            int Option = JOptionPane.showConfirmDialog(null, "You are currently editing row "+prevPoint+", Are you sure to change rows?.", "Currently Editing", JOptionPane.YES_NO_OPTION);
            
            if(Option == 0){
                prevPoint = point;
                panelEditinglbl.setVisible(true);
                lblEdit.setVisible(true);
                lblRow.setText(String.valueOf(point));
                lblRow.setVisible(true);
                shsActionBar.setBorder(shsAbarTxt[1]);
                btnAdd.setText("Update");
                btnCancel.setVisible(true);
                
                data.clear();
                for(int i = 0; i < shsModel.getColumnCount(); i++){
                    data.add(String.valueOf(shsModel.getValueAt(point, i)));
                }
                // Display
                cmbSection.setSelectedItem(data.get(2));
                cmbGradeLevel.setSelectedItem(data.get(3));
                cmbStrand.setSelectedItem(data.get(4));
                cmbYearSHS.setSelectedItem(data.get(0));
                
                ButtonModel btn = data.contains("1st") ? rbSem1.getModel() : rbSem2.getModel();
                BGsem.setSelected(btn, true);
                ButtonModel _btn = data.contains("Completer") ? rbCompleter.getModel() : rbIrregular.getModel();
                BGremarksSHS.setSelected(_btn, true);
                
            }
            
        }else{
            didClick = true;
            prevPoint = point;
            
            // Miscellaneous
            panelEditinglbl.setVisible(true);
            lblEdit.setVisible(true);
            lblRow.setText(String.valueOf(point));
            lblRow.setVisible(true);
            shsActionBar.setBorder(shsAbarTxt[1]);
            btnAdd.setText("Update");
            btnCancel.setVisible(true);
            
            
            if(data.isEmpty()){
                for(int i = 0; i < shsModel.getColumnCount(); i++){
                    data.add(String.valueOf(shsModel.getValueAt(point, i)));
                }
                // Display
                cmbSection.setSelectedItem(data.get(2));
                cmbGradeLevel.setSelectedItem(data.get(3));
                cmbStrand.setSelectedItem(data.get(4));
                cmbYearSHS.setSelectedItem(data.get(0));
                
                ButtonModel btn = data.contains("1st") ? rbSem1.getModel() : rbSem2.getModel();
                BGsem.setSelected(btn, true);
                ButtonModel _btn = data.contains("Completer") ? rbCompleter.getModel() : rbIrregular.getModel();
                BGremarksSHS.setSelected(_btn, true);
            }
        }
        
        
    }//GEN-LAST:event_tblSHSMouseClicked

