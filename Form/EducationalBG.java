    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String txt = btnAdd.getText();
        int pointAt = point;
        
        if(txt.equals("Add")){
            // added block for add event
            int Option = JOptionPane.showConfirmDialog(null, "Add New Senior-High Data to Table?", "Adding..", JOptionPane.YES_NO_OPTION);
            if(Option == 0){
                if((BGsem.isSelected(rbSem1.getModel()) == false && BGsem.isSelected(rbSem2.getModel()) == false)|| 
              (BGremarksSHS.isSelected(rbCompleter.getModel()) == false && BGremarksSHS.isSelected(rbIrregular.getModel()) == false)){
                    
                    JOptionPane.showMessageDialog(this, "One of the fields are null/left blank.", "Invalid Field", 
                    JOptionPane.INFORMATION_MESSAGE);
                }else{
                    String SY = String.valueOf(cmbYearSHS.getSelectedItem());
                    String Sem = BGsem.getSelection().getActionCommand();
                    String gradeLvl = String.valueOf(cmbGradeLevel.getSelectedItem());
                    String Section = String.valueOf(cmbSection.getSelectedItem());
                    String Strand = String.valueOf(cmbStrand.getSelectedItem());
                    String Adviser = "None for now";
                    String Remarks = BGremarksSHS.getSelection().getActionCommand();
                    
                    Object[] data = {SY, Sem, Section,
                                 gradeLvl, Strand, Remarks, Adviser};
                    DefaultTableModel shsModel = (DefaultTableModel) tblSHS.getModel();
                    shsModel.addRow(data);
                }
            }
        }else{
            // update block to work for SQL
            int Option = JOptionPane.showConfirmDialog(null, "Update Senior-High Data to Table?", "Updating..", JOptionPane.YES_NO_OPTION);
            
            if(Option == 0){
                didClick = false;
                data.clear();
                if((BGsem.isSelected(rbSem1.getModel()) == false && BGsem.isSelected(rbSem2.getModel()) == false)|| 
              (BGremarksSHS.isSelected(rbCompleter.getModel()) == false && BGremarksSHS.isSelected(rbIrregular.getModel()) == false)){
                    
                    JOptionPane.showMessageDialog(this, "One of the fields are null/left blank.", "Invalid Field", 
                    JOptionPane.INFORMATION_MESSAGE);
                }else{
                    String SY = String.valueOf(cmbYearSHS.getSelectedItem());
                    String Sem = BGsem.getSelection().getActionCommand();
                    String gradeLvl = String.valueOf(cmbGradeLevel.getSelectedItem());
                    String Section = String.valueOf(cmbSection.getSelectedItem());
                    String Strand = String.valueOf(cmbStrand.getSelectedItem());
                    String Adviser = "None for now";
                    String Remarks = BGremarksSHS.getSelection().getActionCommand();
                    
                    
                    // Add a condition if the previous data is the same as the new data
                    // Then dont do anythin
                    Object[] newData = {SY, Sem, Section,
                                 gradeLvl, Strand, Remarks, Adviser};
                    DefaultTableModel shsModel = (DefaultTableModel) tblSHS.getModel();
                    
                    for(int i = 0; i < shsModel.getColumnCount();i++){
                        shsModel.setValueAt(newData[i], pointAt, i);
                    }
                    panelEditinglbl.setVisible(false);
                    lblEdit.setVisible(false);
                    lblRow.setVisible(false);
                    btnAdd.setText("Add");
                    btnCancel.setVisible(false);
                    shsActionBar.setBorder(shsAbarTxt[0]);
                    cmbYearSHS.setSelectedIndex(YearArr.getArrEndIndex());
                    cmbSection.setSelectedIndex(0);
                    cmbGradeLevel.setSelectedIndex(0);
                    cmbStrand.setSelectedIndex(0);
                    BGsem.clearSelection();
                    BGremarksSHS.clearSelection();
                }
            }
            
        }
    }//GEN-LAST:event_btnAddActionPerformed

